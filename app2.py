st.set_page_config(page_title="Entrenamiento ML - Optimizador", layout="wide")

st.title("🧠 Simulador de Entrenamiento: Optimización de Función de Pérdida")
st.markdown("Ajuste de pesos mediante Gradiente Descendente para modelos de Machine Learning.")

# --- BARRA LATERAL (Hiperparámetros) ---
st.sidebar.header("⚙️ Hiperparámetros de Red")

ecuacion = st.sidebar.text_input("Función de Pérdida f(x,y):", value="(1 - x)**2 + 100 * (y - x**2)**2")
st.sidebar.caption("Nota: Internamente x e y representan los pesos W1 y W2 del modelo.")

lr = st.sidebar.number_input("Tasa de Aprendizaje (Learning Rate):", value=0.001, format="%.5f")
epochs = st.sidebar.slider("Épocas Máximas (Iteraciones):", min_value=100, max_value=10000, value=5000, step=100)
momentum = st.sidebar.slider("Momentum (Inercia):", min_value=0.0, max_value=0.99, value=0.9, step=0.01)

st.sidebar.subheader("📍 Inicialización de Pesos")
usar_aleatorio = st.sidebar.checkbox("Inicialización Aleatoria", value=True)

if usar_aleatorio:
    w1_init = np.random.uniform(-3.0, 3.0)
    w2_init = np.random.uniform(-3.0, 3.0)
    st.sidebar.info(f"Pesos iniciales: W1={w1_init:.2f}, W2={w2_init:.2f}")
else:
    w1_init = st.sidebar.number_input("Peso Inicial W1:", value=-1.2)
    w2_init = st.sidebar.number_input("Peso Inicial W2:", value=1.0)

# --- EJECUCIÓN ---
if st.sidebar.button("▶️ Entrenar Modelo", type="primary"):
    try:
        model = LossFunctionModel(ecuacion)
        optimizer = GradientDescentOptimizer(learning_rate=lr, max_epochs=epochs, momentum=momentum)

        with st.spinner('Entrenando el modelo y ajustando pesos...'):
            results = optimizer.train(model, np.array([w1_init, w2_init]))

        opt_weights = results['optimal_weights']
        min_loss = results['loss_history'][-1]

        # Panel de Métricas
        col1, col2, col3 = st.columns(3)
        col1.metric("Peso Óptimo W1", f"{opt_weights[0]:.5f}")
        col2.metric("Peso Óptimo W2", f"{opt_weights[1]:.5f}")
        col3.metric("Error Final (Loss)", f"{min_loss:.5f}")

        # Diagnóstico
        diagnostico = ModelDiagnostic.analyze(results, epochs)
        if "EXITOSO" in diagnostico:
            st.success(diagnostico)
        elif "DIVERGENCIA" in diagnostico or "ESTANCAMIENTO" in diagnostico:
            st.error(diagnostico)
        else:
            st.warning(diagnostico)

        # Gráficas
        st.subheader("Análisis Visual del Entrenamiento")
        fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(15, 6))

        path = np.array(results['path'])
        px, py = path[:, 0], path[:, 1]
        history = results['loss_history']

        margen = 2.0
        w1_min, w1_max = min(w1_init, opt_weights[0]) - margen, max(w1_init, opt_weights[0]) + margen
        w2_min, w2_max = min(w2_init, opt_weights[1]) - margen, max(w2_init, opt_weights[1]) + margen

        W1, W2 = np.meshgrid(np.linspace(w1_min, w1_max, 200), np.linspace(w2_min, w2_max, 200))
        Loss_Surface = model.evaluate(W1, W2)

        # Gráfica 1: Superficie de Error
        contour = ax1.contour(W1, W2, Loss_Surface, levels=40, cmap='viridis', alpha=0.8)
        fig.colorbar(contour, ax=ax1, label='Magnitud del Error')
        ax1.plot(px, py, marker='.', color='red', linestyle='-', linewidth=1.5, label='Ruta de Ajuste')
        ax1.plot(px[0], py[0], marker='s', color='blue', markersize=8, label='Pesos Iniciales')
        ax1.plot(px[-1], py[-1], marker='*', color='gold', markersize=14, label='Pesos Óptimos')
        ax1.set_title('Topografía de la Función de Pérdida')
        ax1.set_xlabel('Peso W1')
        ax1.set_ylabel('Peso W2')
        ax1.legend()
        ax1.grid(True, alpha=0.3)

        # Gráfica 2: Curva de Convergencia
        ax2.plot(range(len(history)), history, color='purple', linewidth=2)
        ax2.set_title('Curva de Aprendizaje (Convergencia)')
        ax2.set_xlabel('Épocas (Iteraciones)')
        ax2.set_ylabel('Error (Loss)')
        ax2.set_yscale('symlog')
        ax2.grid(True, alpha=0.3)

        plt.tight_layout()
        st.pyplot(fig)

    except Exception as e:
        st.error(f"Error en la estructura matemática de la red: {e}")
else:
    st.info("👈 Configura los hiperparámetros en el panel lateral y presiona 'Entrenar Modelo'.")