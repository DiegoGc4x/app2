import streamlit as st
import sympy as sp
import numpy as np
import matplotlib.pyplot as plt
from typing import Dict

# ==========================================
# 1. NÚCLEO MATEMÁTICO (Cálculo de la Función de Pérdida)
# ==========================================
class LossFunctionModel:
    """Procesa la función de pérdida y calcula gradientes simbólicos."""
    def __init__(self, equation_str: str):
        self.w1, self.w2 = sp.symbols('w1 w2') # w1 y w2 representan los Pesos del modelo
        # Reemplazamos x,y por w1,w2 para que el usuario pueda escribir con x e y por costumbre
        eq_adaptada = equation_str.replace('x', 'w1').replace('y', 'w2')
        self.expr = sp.sympify(eq_adaptada)

        self._f_eval = sp.lambdify((self.w1, self.w2), self.expr, 'numpy')
        self._grad_w1 = sp.lambdify((self.w1, self.w2), sp.diff(self.expr, self.w1), 'numpy')
        self._grad_w2 = sp.lambdify((self.w1, self.w2), sp.diff(self.expr, self.w2), 'numpy')

    def evaluate(self, w1_val, w2_val):
        return self._f_eval(w1_val, w2_val)

    def gradient(self, point: np.ndarray) -> np.ndarray:
        return np.array([float(self._grad_w1(point[0], point[1])),
                         float(self._grad_w2(point[0], point[1]))])

# ==========================================
# 2. MOTOR DE ENTRENAMIENTO (Optimizador)
# ==========================================
class GradientDescentOptimizer:
    """Ajusta los pesos del modelo utilizando Gradiente Descendente con Inercia."""
    def __init__(self, learning_rate=0.001, max_epochs=1000, tolerance=1e-6, momentum=0.9):
        self.lr = learning_rate
        self.max_epochs = max_epochs
        self.tolerance = tolerance
        self.momentum = momentum

    def train(self, model: LossFunctionModel, initial_weights: np.ndarray) -> Dict:
        path = [initial_weights.copy()]
        loss_history = [model.evaluate(initial_weights[0], initial_weights[1])]

        current_weights = initial_weights.copy()
        velocity = np.zeros(2)

        for _ in range(self.max_epochs):
            grad = model.gradient(current_weights)

            # Regla de actualización de pesos
            velocity = self.momentum * velocity - self.lr * grad
            next_weights = current_weights + velocity

            path.append(next_weights.copy())
            loss_val = model.evaluate(next_weights[0], next_weights[1])
            loss_history.append(loss_val)

            # Criterios de parada matemático (Gradiente cercano a cero o error infinito)
            if np.linalg.norm(grad) < self.tolerance or np.isinf(loss_val) or np.isnan(loss_val) or loss_val > 1e15:
                current_weights = next_weights
                break

            current_weights = next_weights

        return {'optimal_weights': current_weights, 'path': path, 'loss_history': loss_history}

# ==========================================
# 3. DIAGNÓSTICO DEL MODELO
# ==========================================
class ModelDiagnostic:
    @staticmethod
    def analyze(results: Dict, max_epochs: int) -> str:
        history = results['loss_history']
        epochs = len(history) - 1

        if np.isnan(history[-1]) or np.isinf(history[-1]) or history[-1] > history[0] * 100:
            return "🚨 **DIVERGENCIA (Explosión del Gradiente):** La Tasa de Aprendizaje es muy alta. El modelo no pudo aprender."

        subidas = sum(1 for i in range(1, len(history)) if history[i] > history[i-1])
        if subidas > epochs * 0.1:
            return "⚠️ **OSCILACIÓN EXCESIVA:** El modelo rebota demasiado en la superficie de error. Reduce el Learning Rate."

        if epochs >= max_epochs:
            return "🐢 **ESTANCAMIENTO:** Faltaron épocas para minimizar la pérdida por completo o el Learning Rate es muy bajo."

        return f"✅ **ENTRENAMIENTO EXITOSO:** Convergencia alcanzada y error minimizado en {epochs} épocas."

